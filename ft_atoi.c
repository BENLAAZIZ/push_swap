#include "push_swap.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned long	f;
	int				s;

	i = 0;
	f = 0;
	s = 1;
	while ((*(str + i) >= 9 && *(str + i) <= 13) || *(str + i) == 32)
		i++;
	if (*(str + i) == '+' || *(str + i) == '-')
	{
		if (*(str + i) == '-')
			s *= -1;
		i++;
	}
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		f = (f * 10) + (*(str + i)) - '0';
		if (f >= 9223372036854775807 && s == 1)
			return (-1);
		if (f > 9223372036854775807 && s == -1)
			return (0);
		i++;
	}
	return ((int)f * s);
}