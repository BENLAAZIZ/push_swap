#include "push_swap.h"

long	ft_atoi(const char *str)
{
	int		i;
	long	f;
	int		s;

	i = 0;
	f = 0;
	s = 1;
	if (*(str + i) == '+' || *(str + i) == '-')
	{
		if (*(str + i) == '-')
			s *= -1;
		i++;
	}
	while (*(str + i) >= '0' && *(str + i) <= '9' && f < 2147483649)
	{
		f = (f * 10) + (*(str + i)) - '0';
		i++;
	}
	return (f * s);
}
